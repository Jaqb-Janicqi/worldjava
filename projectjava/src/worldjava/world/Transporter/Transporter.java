package worldjava.world.Transporter;

public class Transporter 
{
    public int id;
    public int posX;
    public int posY;
    public Boolean animal;
    
    public Transporter(int id, int posX, int posY, Boolean animal)
    {
        this.id = id;
        this.posX = posX;
        this.posY = posY;
        this.animal = animal;
    }
}
